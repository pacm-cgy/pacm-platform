// §9. 메인 롱폼 빌더 (v10)
// ══════════════════════════════════════════════════════════════════════

function buildLongformStory(title, body) {
  const cleanBody  = cleanText(body || '')
  const eventType  = detectEvent(title, cleanBody)
  const domain     = detectDomain(title, cleanBody)
  const sentences  = splitSentences(cleanBody).filter(s => !isNoise(s))
  const keySents   = extractKeySentences(title, sentences, 10)
  const ner        = parseTitle(title)
  const domCtx     = DOMAIN_CONTEXT[domain] || DOMAIN_CONTEXT.startup
  const evtInfo    = EVENT_TYPES[eventType] || EVENT_TYPES.market

  // 문장 분배
  const topSents  = keySents.slice(0, 3)
  const midSents  = keySents.slice(3, 7)
  const lastSents = keySents.slice(7)

  // 용어 수집 (최대 4개)
  const fullText = title + ' ' + cleanBody
  const usedTerms = []
  for (const [term, info] of Object.entries(TERM_DICT)) {
    if (fullText.includes(term) && usedTerms.length < 4) usedTerms.push({ term, ...info })
  }

  // 심층 질문
  const deepQs = getDeepQuestions(eventType, domain)

  const lines = []

  // ── SECTION 0: 카테고리 배지 ─────────────────────────────────────
  lines.push(`## ${evtInfo.emoji} ${evtInfo.label}`)
  lines.push(``)

  // ── SECTION 1: 도입 ──────────────────────────────────────────────
  const introLines = buildIntro(title, ner, eventType, domain)
  for (const l of introLines) lines.push(l)
  lines.push(``)

  if (topSents.length > 0) {
    lines.push(`> ${topSents[0]}`)
    lines.push(``)
  }

  // ── SECTION 2: 지금 무슨 일이 일어났나 ──────────────────────────
  lines.push(`---`)
  lines.push(``)
  lines.push(`## 📌 지금 무슨 일이 일어났나`)
  lines.push(``)

  if (midSents.length > 0) {
    for (const s of midSents) {
      lines.push(`> ${s}`)
      lines.push(``)
    }
  } else {
    // 본문 없는 경우 — 제목 기반 사실 요약
    lines.push(buildFactSummary(title, ner, eventType))
    lines.push(``)
  }

  if (topSents.length > 1) {
    lines.push(topSents[1])
    lines.push(``)
  }
  if (topSents.length > 2) {
    lines.push(topSents[2])
    lines.push(``)
  }

  // ── SECTION 3: 배경 지식 ─────────────────────────────────────────
  lines.push(`---`)
  lines.push(``)
  lines.push(`## 🔍 이걸 이해하려면 이 배경을 알아야 해`)
  lines.push(``)
  lines.push(domCtx.background)
  lines.push(``)

  if (lastSents.length > 0) {
    lines.push(`**기사에서 놓치지 말아야 할 내용:**`)
    lines.push(``)
    for (const s of lastSents) {
      lines.push(`• ${s}`)
      lines.push(``)
    }
  }

  // ── SECTION 4: 시장 데이터 & 트렌드 ─────────────────────────────
  lines.push(`---`)
  lines.push(``)
  lines.push(`## 📊 시장은 지금 어떻게 돌아가고 있나`)
  lines.push(``)
  lines.push(domCtx.market)
  lines.push(``)
  lines.push(`**지금 이 분야의 가장 뜨거운 변화:**`)
  lines.push(``)
  lines.push(domCtx.trend)
  lines.push(``)

  // ── SECTION 5: 용어 해설 ─────────────────────────────────────────
  if (usedTerms.length > 0) {
    lines.push(`---`)
    lines.push(``)
    lines.push(`## 📚 몰라도 되지만, 알면 더 잘 보이는 개념들`)
    lines.push(``)
    lines.push(`*처음 보는 단어가 있어도 괜찮습니다. 지금 이 자리에서 이해하면 됩니다. 모르는 게 부끄러운 게 아니라, 알려고 하지 않는 게 아쉬운 것입니다.*`)
    lines.push(``)
    for (const { term, short, explain } of usedTerms) {
      lines.push(`**${short}**`)
      lines.push(``)
      lines.push(explain)
      lines.push(``)
    }
  }

  // ── SECTION 6: 창업가의 시선 ─────────────────────────────────────
  lines.push(`---`)
  lines.push(``)
  lines.push(`## 💡 창업가의 시선으로 읽으면`)
  lines.push(``)
  lines.push(`*같은 뉴스도 어떤 눈으로 읽느냐에 따라 완전히 다른 의미가 됩니다. 뉴스를 소비하는 사람과 뉴스에서 기회를 찾는 사람의 차이입니다.*`)
  lines.push(``)
  lines.push(domCtx.implication)
  lines.push(``)

  // ── SECTION 7: 깊이 생각하기 ─────────────────────────────────────
  lines.push(`---`)
  lines.push(``)
  lines.push(`## 💭 이 뉴스를 읽고 나서 던져볼 질문 3가지`)
  lines.push(``)
  lines.push(`*노트를 꺼내 적어보세요. 생각을 글로 쓰면, 막연한 것이 구체적이 됩니다.*`)
  lines.push(``)
  for (const q of deepQs) {
    lines.push(`• **Q.** ${q}`)
    lines.push(``)
  }

  lines.push(`---`)
  lines.push(``)
  lines.push(`*매일 뉴스 하나를 이렇게 깊이 읽는 사람과, 그냥 스크롤하는 사람의 차이는 1년 후 엄청나게 벌어집니다.*`)
  lines.push(``)
  lines.push(`*Insightship · ${DOMAINS[domain]?.ko || domain} · ${evtInfo.label}*`)

  return lines.join('\n')
}

