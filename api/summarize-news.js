// 본문 없을 때 제목 기반 사실 요약 (buildLongformStory 에서 참조하므로 앞으로 이동)
function buildFactSummary(title, ner, eventType) {
  const { amounts, geo, tech, stage, orgs, action } = ner
  const parts = []
  if (orgs.length > 0) parts.push(`**${orgs[0]}**이(가) 이번 소식의 주인공입니다.`)
  if (action === 'invest') {
    if (amounts.length > 0) parts.push(`이번에 **${amounts[0]}**의 투자를 유치했습니다.`)
    if (stage) parts.push(`투자 단계는 **${stage}**입니다.`)
    if (geo.length > 0 && !['한국','국내'].includes(geo[0])) parts.push(`**${geo[0]}** 지역과의 연계가 주목됩니다.`)
  } else if (action === 'contest') {
    if (geo.length > 0) parts.push(`**${geo[0]}**에서 유망 스타트업 육성 프로그램에 최종 선정됐습니다.`)
    if (tech.length > 0) parts.push(`**${tech[0]}** 기술 분야의 혁신성이 높이 평가받았습니다.`)
  } else if (action === 'analysis') {
    if (tech.length > 0) parts.push(`**${tech[0]}** 시장에 대한 중요한 분석 결과입니다.`)
    if (geo.length > 0) parts.push(`**${geo[0]}** 생태계에 미치는 영향이 핵심입니다.`)
  } else if (action === 'acquire') {
    if (amounts.length > 0) parts.push(`**${amounts[0]}** 규모의 인수·합병이 이루어졌습니다.`)
  } else if (action === 'launch') {
    if (tech.length > 0) parts.push(`**${tech[0]}** 기반의 새로운 서비스가 출시됐습니다.`)
  }
  if (parts.length === 0) return `이번 소식의 핵심은 **"${title}"**입니다. 이 뉴스의 배경과 의미를 아래에서 깊이 살펴봅니다.`
  return parts.join(' ')
}

