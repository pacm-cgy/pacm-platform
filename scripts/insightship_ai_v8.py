def clean(text: str) -> str:
    if not text: return ''
    text = re.sub(r'<[^>]+>', ' ', text)
    text = re.sub(r'https?://\S+', '', text)
    text = re.sub(r'&\w+;', ' ', text)
    text = re.sub(r'[^\w\s가-힣a-zA-Z0-9.,!?%·→■●]', ' ', text)
    return re.sub(r'\s+', ' ', text).strip()

def sent_split(text: str) -> List[str]:
    parts = re.split(r'(?<=[.!?。])\s+', text)
    return [s.strip() for s in parts if len(s.strip()) > 12]

def top_keywords(text: str, n: int = 20) -> List[str]:
    words = re.findall(r'[가-힣a-zA-Z]{2,}', text)
    cnt = Counter(w for w in words if w not in STOPWORDS and len(w) > 1)
    return [w for w, _ in cnt.most_common(n)]

def extract_numbers(text: str) -> List[str]:
    nums = re.findall(r'\d+(?:,\d+)*(?:\.\d+)?(?:억|조|만|천|백|%|달러|원|명|개|곳)?', text)
    seen, result = set(), []
    for n in nums:
        if n not in seen and len(n) > 1:
            seen.add(n); result.append(n)
    return result[:8]

def detect_event(text: str) -> str:
    scores = {ev: sum(2 if kw in text else 0 for kw in kws)
              for ev, kws in EVENT_KW.items()}
    best = max(scores, key=scores.get)
    return best if scores[best] > 0 else "general"

def detect_domain(text: str) -> str:
    scores = {dm: sum(1 for kw in kws if kw in text)
              for dm, kws in DOMAIN_KW.items()}
    best = max(scores, key=scores.get)
    return best if scores[best] > 0 else "startup"

def find_terms(text: str) -> List[Tuple]:
    found = []
    for key, (name, desc) in TERMS.items():
        if key in text or key.lower() in text.lower():
            found.append((key, name, desc))
    return found[:5]

def best_sents(sents: List[str], keywords: List[str], n: int = 5) -> List[str]:
    def score(s: str) -> float:
        kw_hit = sum(1 for k in keywords if k in s) / max(len(keywords), 1)
        has_num = 1.3 if re.search(r'\d', s) else 1.0
        noise = 0.3 if any(w in s for w in ["기자","무단","저작권","Copyright","©"]) else 1.0
        length = min(len(s) / 50, 2.0)
        return kw_hit * has_num * noise * length
    scored = sorted([(s, score(s)) for s in sents if len(s) > 15], key=lambda x: -x[1])
    result = []
    for s, sc in scored:
        if not any(s[:20] in r for r in result):
            result.append(s)
            if len(result) >= n: break
    return result

def pick(lst: list, seed: str) -> object:
    """시드 기반 결정론적 선택 (같은 기사는 항상 같은 앵글)"""
    idx = int(hashlib.md5(seed.encode()).hexdigest(), 16) % len(lst)
    return lst[idx]

